package java24hours;

class PlanetWEight{
    public static void main(String[] arguments){
     System.out.println("Your weight on the Earth will be ");
     double weight = 80;
     System.out.println(weight);
     
     System.out.println("Your weight on the Mercury will be ");
     double m_weight = weight*.378;
     System.out.println(m_weight);
     
     System.out.println("Your weight on the Moon will be ");
     double mn_weight = weight*.166;
     System.out.println(mn_weight);
     
     System.out.println("Your weight on the Upi will be ");
     double u_weight = weight*2.364;
     System.out.println(u_weight);
     
     System.out.println("Your weight on the Venera will be ");
     double v_weight = weight*.907;
     System.out.println(v_weight);
     
     System.out.println("Your weight on the Uranus will be ");
     double ur_weight = weight*.889;
     System.out.println(ur_weight);
    }
}
